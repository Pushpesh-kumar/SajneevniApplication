    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        boolean isInputValid=validateInputs();
        if(!isInputValid){
            JOptionPane.showMessageDialog(null,"PLEASE ENTER NAME AND SALARY","INPUT FAILURE",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try
        {
            double esal=Double.parseDouble(txtEmpSal.getText());
            EmpPojo emp=new EmpPojo();
            emp.setEmpId(txtEmpId.getText().trim());
            emp.setEmpName(txtEmpName.getText().trim());
            emp.setEmpSal(esal);
            emp.setEmpDepartment(jcEmpJob.getSelectedItem().toString());
            boolean result=EmpDao.addEmployee(emp);
            if(result){
                JOptionPane.showMessageDialog(this,"EMPLOYEE REGISTERED");
                
                txtEmpId.setText(EmpDao.getNextEmpId());
                txtEmpName.setText("");
                txtEmpSal.setText("");
                
            }
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DATABASE ERROR","Error", JOptionPane.ERROR_MESSAGE);
            
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(this,"SALARY FORMAT IS NUMERICAL","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

