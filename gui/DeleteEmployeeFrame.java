    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try{

            if(EmpDao.deleteEmployee(getSelectedId())){

                JOptionPane.showMessageDialog(this,"EMPLOYEE REMOVED","DELETE",JOptionPane.INFORMATION_MESSAGE);
                jcEmpId.removeItem(jcEmpId.getSelectedItem());
            }
                  if(jcEmpId.getSelectedIndex()==-1){
                    JOptionPane.showMessageDialog(this,"NO EMPLOYEE REGISTERED");
                    new ManageEmployeeFrame().setVisible(true);
                    this.dispose();
                    return;
            }               

        }catch(SQLException ex){
            JOptionPane.showMessageDialog(this,"DATABASE ERROR","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
       
    }//GEN-LAST:event_btnDeleteActionPerformed

