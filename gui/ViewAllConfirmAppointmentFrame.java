        private void loadAllPatientDetails() {
        try{
            List<AppointmentPojo>confList=AppointmentDao.confirmAppointmentList(UserProfile.getUserName());
            if(confList.isEmpty()){
                JOptionPane.showMessageDialog(null,"No Patient has appointment");
                return;
            }
            Object [] obj=new Object[12];
            for(AppointmentPojo pat:confList){
                obj[0]=pat.getPatientId();
                obj[1]=pat.getPatientName();
                obj[2]=pat.getOdp();
                obj[3]=pat.getStatus();
                obj[4]=pat.getOdp();
                model.addRow(obj);
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error In DB");
            ex.printStackTrace();
             
        }
    }
